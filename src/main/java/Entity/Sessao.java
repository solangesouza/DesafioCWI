package Entity;

public class Sessao {
	
	private int id_sessao;
	private int id_pauta;
	private int tempo;
	public int getId_sessao() {
		return id_sessao;
	}
	public void setId_sessao(int id_sessao) {
		this.id_sessao = id_sessao;
	}
	public int getId_pauta() {
		return id_pauta;
	}
	public void setId_pauta(int id_pauta) {
		this.id_pauta = id_pauta;
	}
	public int getTempo() {
		return tempo;
	}
	public void setTempo(int tempo) {
		this.tempo = tempo;
	}
	
		
	
	
	
}
